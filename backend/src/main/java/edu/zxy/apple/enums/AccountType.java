package edu.zxy.apple.enums;

/**
 * ACCOUNT_TYPE（账户类型）
 * 1.CASH（现金）
 * 2.CREDIT_CARD（信用卡）
 * 3.SAVINGS（储蓄）
 * 4.PREPAID（预付费账户，例如：健身计次卡，停车月卡）
 * 5.DEBT（债务：别人借给我钱）
 * 6.LOAN（债权：我借给别人钱）
 * 7.ONLINE（非银行的第三方网络金融机构，例如：支付宝，微信）
 * 
 * @author 57359
 *
 */
public enum AccountType
{
    CASH, CREDIT_CARD, SAVINGS, PREPAID, DEBT, LOAN, ONLINE;
}
